public class TennisPlayer {
    private final int age;
    private final int score;
    private final String name;

    public TennisPlayer(int age, int score, String name) {
        this.age = age;
        this.score = score;
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public int getScore() {
        return score;
    }

    public String getName() {
        return name;
    }
}




